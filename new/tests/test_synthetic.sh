#!/bin/bash

# Create synthetic reads that should align to MUC20 and DECR2 genes
# MUC20: chr3:195720884-195741123
# DECR2: chr16:401858-412487

# Create R2 file (cDNA reads) with sequences that might align to these regions
cat > synthetic_R2.fastq << 'EOF'
@MUC20_synthetic_read/2
AAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAA
+
IIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIII
@DECR2_synthetic_read/2
TTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTT
+
IIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIII
EOF

# Create R1 file (cell barcode + UMI) 
cat > synthetic_R1.fastq << 'EOF'
@MUC20_synthetic_read/1
AAACCCTTTGGGAAAC
+
IIIIIIIIIIIIIIII
@DECR2_synthetic_read/1
AAACCCTTTGGGAAAC
+
IIIIIIIIIIIIIIII
EOF

# Compress the files
gzip -f synthetic_R1.fastq
gzip -f synthetic_R2.fastq

echo "Created synthetic_R1.fastq.gz and synthetic_R2.fastq.gz"
